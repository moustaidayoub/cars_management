    private void btnPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPasswordActionPerformed
       
                String matricule=txtMatricule.getText();
        user=uDAO.read(matricule);
        String password="";
        if(txtPassword.getPassword().length>0)
        {
        for(int i=0;i<txtPassword.getPassword().length;i++)
            password=password+txtPassword.getPassword()[i];
        }
        if(matricule.equals(user.getMatriculeUser())&&password.equals(user.getPassword()))
{
IHMAccueil ihmAccueil=new IHMAccueil();    
this.dispose();
ihmAccueil.setVisible(true);
}
        else {
            JOptionPane.showMessageDialog(this, "Authentification refusÃ©e : DonnÃ©es incorrectes");
        txtPassword.setText("");
        }
    }//GEN-LAST:event_btnPasswordActionPerformed

